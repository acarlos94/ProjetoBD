    public void lerTabela() {
        DefaultTableModel modelo = (DefaultTableModel) jTableTabelaTrabalhos.getModel();
        modelo.setNumRows(0);
        TrabalhosDao tdao = new TrabalhosDao();

        for (ArrayList a : tdao.read2()) {
            modelo.addRow(new Object[]{
                a.get(0),
                a.get(1),
                a.get(2),
                a.get(3),
                a.get(4),
                a.get(5)

            });
        }
    }

